    private void loadEvent(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_loadEvent
        String sql = "SELECT * FROM events where event_title = ?";
        try{
            PreparedStatement statement = sql_connect.db_connect().prepareStatement(sql);
            statement.setString(1,variable.eventToEdit);
            
            ResultSet resultSet = statement.executeQuery();
            
            if(resultSet.next()){
               String initialDate = resultSet.getString("event_date"); // Replace with your desired initial date
            java.util.Date parsedDate;
            try {
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                parsedDate = dateFormat.parse(initialDate);
                dateOfEvent.setDate(parsedDate);
            } catch (ParseException e) {
                e.printStackTrace();
            }
               eventTitle.setText(resultSet.getString("event_title"));
               duration.setText(resultSet.getString("event_duration"));
               description.setText(resultSet.getString("event_desc"));   
            }
                  
        }catch(SQLException ex){
            
        }
    }//GEN-LAST:event_loadEvent

