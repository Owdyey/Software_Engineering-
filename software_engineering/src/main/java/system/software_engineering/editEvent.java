    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int eventId = 0;
        String sql1 = "SELECT event_id FROM events where event_title = ?";
        String sql = "UPDATE events SET event_title = ?, event_date = ?, event_duration = ?,event_desc = ? WHERE event_id = ?";
        
        
        try{
            PreparedStatement statement = sql_connect.db_connect().prepareStatement(sql1);
            statement.setString(1, variable.eventToEdit);
            
            ResultSet resultSet = statement.executeQuery();
            
            if(resultSet.next()){
               eventId = resultSet.getInt("event_id");
            }
        }catch(SQLException ex){
            System.out.print(ex);
        }
        
        try{
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String eventDate = dateFormat.format(dateOfEvent.getDate());
            PreparedStatement statement = sql_connect.db_connect().prepareStatement(sql);
            
            statement.setString(1, eventTitle.getText());
            statement.setString(2, eventDate);
            statement.setString(3, duration.getText());
            statement.setString(4,description.getText());
            statement.setInt(5, eventId);
            statement.executeUpdate();
            
            JOptionPane.showMessageDialog(null,
                    "Event updated successfully!", 
                    "Successful", 
                    JOptionPane.INFORMATION_MESSAGE);
               
        }catch(SQLException ex){
            System.out.print(ex);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

